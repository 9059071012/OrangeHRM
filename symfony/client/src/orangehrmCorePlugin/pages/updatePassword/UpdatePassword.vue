<template>
  <div class="orangehrm-background-container">
    <div class="orangehrm-card-container">
      <oxd-text tag="h6" class="orangehrm-main-title">Change Password</oxd-text>
      <oxd-divider />

      <oxd-form :loading="isLoading" @submitValid="onSave">
        <oxd-form-row>
          <oxd-grid :cols="2" class="orangehrm-full-width-grid">
            <oxd-grid-item>
              <oxd-input-group :label="$t('core.user_name')">
                <oxd-text class="orangehrm-user-name" tag="p">
                  {{ userName }}
                </oxd-text>
              </oxd-input-group>
            </oxd-grid-item>

            <oxd-grid-item>
              <oxd-input-field
                type="password"
                :label="$t('core.current_password')"
                v-model="user.currentPassword"
                :rules="rules.currentPassword"
                required
              />
            </oxd-grid-item>
          </oxd-grid>
        </oxd-form-row>

        <password-input
          v-model:password="user.password"
          v-model:passwordConfirm="user.passwordConfirm"
        />

        <oxd-divider />
        <oxd-form-actions>
          <required-text />
          <oxd-button
            type="button"
            displayType="ghost"
            label="Cancel"
            @click="onCancel"
          />
          <submit-button />
        </oxd-form-actions>
      </oxd-form>
    </div>
  </div>
</template>

<script>
import {APIService} from '@/core/util/services/api.service';
import {navigate} from '@orangehrm/core/util/helper/navigation';
import PasswordInput from '@/core/components/inputs/PasswordInput';
import {
  required,
  shouldNotExceedCharLength,
} from '@orangehrm/core/util/validation/rules';

const userModel = {
  currentPassword: '',
  password: '',
  passwordConfirm: '',
};

export default {
  props: {
    userName: {
      type: String,
      required: true,
    },
  },
  setup() {
    const http = new APIService(
      window.appGlobal.baseUrl,
      'api/v2/core/update-password',
    );
    return {
      http,
    };
  },
  components: {
    'password-input': PasswordInput,
  },

  data() {
    return {
      isLoading: false,
      user: {...userModel},
      rules: {
        currentPassword: [required, shouldNotExceedCharLength(64)],
      },
    };
  },

  methods: {
    onCancel() {
      navigate('/pim/viewEmployeeList');
    },
    onSave() {
      this.isLoading = true;
      this.http.http
        .put('api/v2/core/update-password', {
          currentPassword: this.user.currentPassword,
          newPassword: this.user.password,
        })
        .then(response => {
          if (response.status === 200) {
            this.$toast.saveSuccess();
          } else {
            this.isLoading = false;
            this.$toast.warn({
              title: 'Warning',
              message: 'Current Password is Incorrect',
            });
            return Promise.reject();
          }
        })
        .then(() => {
          this.user = {...userModel};
          this.onCancel();
        });
    },
  },
};
</script>
